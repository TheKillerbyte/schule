package neben;


public class VariablenTypen {
	@SuppressWarnings("unused")
	public static void main(String[] args) {
		boolean wahrOderFalsch = true; // false
		
		byte maximalByte = Byte.MAX_VALUE; // 8 Bit
		byte minimumByte = Byte.MIN_VALUE; // min = -128 max = 127
		
		short maximalShort = Short.MAX_VALUE; // 16 Bit
		short minimalShort = Short.MIN_VALUE; // min = -32768 max = 32767
		
		int maximalInteger = Integer.MAX_VALUE ; // 32 Bit
		int minimalInteger = Integer.MIN_VALUE; // min = -2147483648 max = 2147483647
		
		long maximalLong = Long.MAX_VALUE; // 64 Bit
		long minimumLong = Long.MIN_VALUE; // min = -9223372036854775808 max = 9223372036854775807

		float maximalFloat = Float.MAX_VALUE; // 32 Bit
		float minimalFloat = Float.MIN_VALUE; // min = 1.4E-45 max = 3.4028235E38
		
		double maximalDouble = Double.MAX_VALUE; // 64 Bit
		double minimalDouble = Double.MIN_VALUE; // min =  1.7976931348623157E308 max = 4.9E-324
		
		char someChar = 'a'; // a b c d e f g h i j k l m n o p q r s t u v w x y z 1 2 3 4 5 6 7 8 9 0 ß ? ! " § $ % & / ( ) = + * # ' - _ . : , ;
		
		
		
	}
}
